public class TargetAchievementProgress {
    String target="";
    String achievement="";
    String percentage;
    String progressValueTargetAchievement="";
    String productivity="";
    String progressValueProductivity="";

    public String getTarget() {
        return target;
    }

    public void setTarget(String target) {
        this.target = target;
    }

    public String getAchievement() {
        return achievement;
    }

    public void setAchievement(String achievement) {
        this.achievement = achievement;
    }

    public String getPercentage() {
        return percentage;
    }

    public void setPercentage(String percentage) {
        this.percentage = percentage;
    }

    public String getProgressValueTargetAchievement() {
        return progressValueTargetAchievement;
    }

    public void setProgressValueTargetAchievement(String progressValueTargetAchievement) {
        this.progressValueTargetAchievement = progressValueTargetAchievement;
    }

    public String getProductivity() {
        return productivity;
    }

    public void setProductivity(String productivity) {
        this.productivity = productivity;
    }

    public String getProgressValueProductivity() {
        return progressValueProductivity;
    }

    public void setProgressValueProductivity(String progressValueProductivity) {
        this.progressValueProductivity = progressValueProductivity;
    }
}
